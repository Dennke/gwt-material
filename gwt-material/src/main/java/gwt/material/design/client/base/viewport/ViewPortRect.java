package gwt.material.design.client.base.viewport;

public class ViewPortRect {

    private int width;
    private int height;

    public ViewPortRect(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
